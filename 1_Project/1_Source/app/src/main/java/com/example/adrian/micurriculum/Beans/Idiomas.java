public class Idiomas {
    private String Idioma;
    private String Nivel;

    public Idiomas(String idioma, String nivel) {
        Idioma = idioma;
        Nivel = nivel;
    }

    public String getIdioma() {
        return Idioma;
    }

    public String getNivel() {
        return Nivel;
    }
}
